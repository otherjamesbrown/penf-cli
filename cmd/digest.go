var (
	digestOutput  string
	digestProject string
	digestDate    string
	digestSince   string
	digestUntil   string
	digestType    string
	digestLimit   int
	digestFocus   string
)

// DigestCommandDeps holds dependencies for digest commands.
type DigestCommandDeps struct {
	Config     *config.CLIConfig
	LoadConfig func() (*config.CLIConfig, error)
}

// DefaultDigestDeps returns the default dependencies.
func DefaultDigestDeps() *DigestCommandDeps {
	return &DigestCommandDeps{
		LoadConfig: config.LoadConfig,
	}
}

// NewDigestCommand creates the root digest command with all subcommands.
func NewDigestCommand(deps *DigestCommandDeps) *cobra.Command {
	if deps == nil {
		deps = DefaultDigestDeps()
	}

	cmd := &cobra.Command{
		Use:   "digest",
		Short: "Manage project digests — automated daily/weekly summaries",
		Long: `Digests are automated narrative summaries of project activity. A daily digest
aggregates attributed content, extracted assertions, instruction matches, and
session ledger entries into a structured summary generated by the LLM.`,
		Aliases: []string{"digests"},
	}

	cmd.PersistentFlags().StringVarP(&digestOutput, "output", "o", "", "Output format: text, json, yaml")

	cmd.AddCommand(newDigestGenerateCommand(deps))
	cmd.AddCommand(newDigestShowCommand(deps))
	cmd.AddCommand(newDigestLatestCommand(deps))
	cmd.AddCommand(newDigestListCommand(deps))

	return cmd
}

// ==================== Generate ====================

func newDigestGenerateCommand(deps *DigestCommandDeps) *cobra.Command {
	cmd := &cobra.Command{
		Use:   "generate <project>",
		Short: "Trigger digest generation for a project",
		Long: `Generate a digest for a project. Supports daily, weekly, and journal types.
Daily and weekly digests are idempotent — if a digest already exists for the date, it is returned.
Journal entries are always new and can be directed by a --focus question.`,
		Args: cobra.ExactArgs(1),
		RunE: func(cmd *cobra.Command, args []string) error {
			return runDigestGenerate(cmd.Context(), deps, args[0])
		},
	}

	cmd.Flags().StringVar(&digestDate, "date", "", "Date for digest generation (YYYY-MM-DD, default: today)")
	cmd.Flags().StringVar(&digestType, "type", "daily", "Digest type: daily, weekly, journal")
	cmd.Flags().StringVar(&digestFocus, "focus", "", "Focus question for journal entries")

	return cmd
}

func runDigestGenerate(ctx context.Context, deps *DigestCommandDeps, project string) error {
	cfg, err := deps.LoadConfig()
	if err != nil {
		return fmt.Errorf("loading configuration: %w", err)
	}
	deps.Config = cfg

	conn, err := connectToGateway(cfg)
	if err != nil {
		return fmt.Errorf("connecting to gateway: %w", err)
	}
	defer conn.Close()

	client := digestv1.NewDigestServiceClient(conn)

	tenantID, err := resolveTenantID(deps.Config)
	if err != nil {
		return err
	}

	date := digestDate
	if date == "" {
		date = time.Now().Format("2006-01-02")
	}

	resp, err := client.TriggerDigest(ctx, &digestv1.TriggerDigestRequest{
		TenantId:    tenantID,
		ProjectName: project,
		Date:        date,
		DigestType:  digestType,
		Focus:       digestFocus,
	})
	if err != nil {
		return fmt.Errorf("triggering digest: %w", err)
	}

	format := resolveOutputFormat(digestOutput, cfg)

	if resp.AlreadyExists {
		switch format {
		case config.OutputFormatJSON:
			out := map[string]interface{}{
				"already_exists": true,
				"digest_id":     resp.DigestId,
			}
			data, _ := json.MarshalIndent(out, "", "  ")
			fmt.Println(string(data))
		case config.OutputFormatYAML:
			out := map[string]interface{}{
				"already_exists": true,
				"digest_id":     resp.DigestId,
			}
			data, _ := yaml.Marshal(out)
			fmt.Print(string(data))
		default:
			fmt.Printf("Digest already exists for %s on %s (id: %s)\n", project, date, resp.DigestId)
		}
		return nil
	}

	switch format {
	case config.OutputFormatJSON:
		out := map[string]interface{}{
			"workflow_id": resp.WorkflowId,
			"project":     project,
			"date":        date,
		}
		data, _ := json.MarshalIndent(out, "", "  ")
		fmt.Println(string(data))
	case config.OutputFormatYAML:
		out := map[string]interface{}{
			"workflow_id": resp.WorkflowId,
			"project":     project,
			"date":        date,
		}
		data, _ := yaml.Marshal(out)
		fmt.Print(string(data))
	default:
		fmt.Printf("Digest generation started for %s on %s (workflow: %s)\n", project, date, resp.WorkflowId)
	}
	return nil
}

