// ==================== List ====================

func newDigestListCommand(deps *DigestCommandDeps) *cobra.Command {
	cmd := &cobra.Command{
		Use:   "list",
		Short: "List digests for a project",
		RunE: func(cmd *cobra.Command, args []string) error {
			return runDigestList(cmd.Context(), deps)
		},
	}

	cmd.Flags().StringVar(&digestProject, "project", "", "Project name (required)")
	cmd.Flags().StringVar(&digestSince, "since", "", "Start date (YYYY-MM-DD)")
	cmd.Flags().StringVar(&digestUntil, "until", "", "End date (YYYY-MM-DD)")
	cmd.Flags().StringVar(&digestType, "type", "", "Filter by digest type")
	cmd.Flags().IntVar(&digestLimit, "limit", 20, "Maximum results")

	_ = cmd.MarkFlagRequired("project")

	return cmd
}

func runDigestList(ctx context.Context, deps *DigestCommandDeps) error {
	cfg, err := deps.LoadConfig()
	if err != nil {
		return fmt.Errorf("loading configuration: %w", err)
	}
	deps.Config = cfg

	conn, err := connectToGateway(cfg)
	if err != nil {
		return fmt.Errorf("connecting to gateway: %w", err)
	}
	defer conn.Close()

	client := digestv1.NewDigestServiceClient(conn)

	tenantID, err := resolveTenantID(deps.Config)
	if err != nil {
		return err
	}

	resp, err := client.ListDigests(ctx, &digestv1.ListDigestsRequest{
		TenantId:    tenantID,
		ProjectName: digestProject,
		Since:       digestSince,
		Until:       digestUntil,
		DigestType:  digestType,
		Limit:       int32(digestLimit),
	})
	if err != nil {
		return fmt.Errorf("listing digests: %w", err)
	}

	format := resolveOutputFormat(digestOutput, cfg)

	switch format {
	case config.OutputFormatJSON:
		items := make([]map[string]interface{}, len(resp.Digests))
		for i, d := range resp.Digests {
			items[i] = map[string]interface{}{
				"id":           d.Id,
				"project_id":   d.ProjectId,
				"project_name": d.ProjectName,
				"digest_type":  d.DigestType,
				"period_start": d.PeriodStart,
				"period_end":   d.PeriodEnd,
				"created_at":   d.CreatedAt,
			}
		}
		data, _ := json.MarshalIndent(items, "", "  ")
		fmt.Println(string(data))
	case config.OutputFormatYAML:
		items := make([]map[string]interface{}, len(resp.Digests))
		for i, d := range resp.Digests {
			items[i] = map[string]interface{}{
				"id":           d.Id,
				"project_name": d.ProjectName,
				"digest_type":  d.DigestType,
				"period_start": d.PeriodStart,
				"created_at":   d.CreatedAt,
			}
		}
		data, _ := yaml.Marshal(items)
		fmt.Print(string(data))
	default:
		if len(resp.Digests) == 0 {
			fmt.Println("No digests found.")
			return nil
		}
		fmt.Printf("%-16s %-8s %-12s %-20s %s\n", "ID", "TYPE", "PERIOD", "PROJECT", "CREATED")
		for _, d := range resp.Digests {
			fmt.Printf("%-16s %-8s %-12s %-20s %s\n",
				truncateString(d.Id, 16),
				d.DigestType,
				d.PeriodStart,
				truncateString(d.ProjectName, 20),
				d.CreatedAt,
			)
		}
	}
	return nil
}

// ==================== Helpers ====================

func outputDigestDetail(d *digestv1.DigestDetail, format config.OutputFormat) {
	if d == nil {
		fmt.Println("No digest found.")
		return
	}

	switch format {
	case config.OutputFormatJSON:
		out := map[string]interface{}{
			"id":                 d.Id,
			"tenant_id":         d.TenantId,
			"project_id":        d.ProjectId,
			"project_name":      d.ProjectName,
			"digest_type":       d.DigestType,
			"period_start":      d.PeriodStart,
			"period_end":        d.PeriodEnd,
			"body":              json.RawMessage(d.Body),
			"model_used":        d.ModelUsed,
			"input_token_count":  d.InputTokenCount,
			"output_token_count": d.OutputTokenCount,
			"created_at":        d.CreatedAt,
		}
		data, _ := json.MarshalIndent(out, "", "  ")
		fmt.Println(string(data))
	case config.OutputFormatYAML:
		out := map[string]interface{}{
			"id":           d.Id,
			"project_name": d.ProjectName,
			"digest_type":  d.DigestType,
			"period_start": d.PeriodStart,
			"period_end":   d.PeriodEnd,
			"body":         d.Body,
			"model_used":   d.ModelUsed,
			"created_at":   d.CreatedAt,
		}
		data, _ := yaml.Marshal(out)
		fmt.Print(string(data))
	default:
		fmt.Printf("Digest: %s\n", d.Id)
		fmt.Printf("  Project:  %s (id: %d)\n", d.ProjectName, d.ProjectId)
		fmt.Printf("  Type:     %s\n", d.DigestType)
		fmt.Printf("  Period:   %s to %s\n", d.PeriodStart, d.PeriodEnd)
		fmt.Printf("  Model:    %s\n", d.ModelUsed)
		fmt.Printf("  Tokens:   %d in / %d out\n", d.InputTokenCount, d.OutputTokenCount)
		fmt.Printf("  Created:  %s\n", d.CreatedAt)
		fmt.Println()
		// Pretty-print the body JSON
		var bodyParsed interface{}
		if err := json.Unmarshal([]byte(d.Body), &bodyParsed); err == nil {
			prettyBody, _ := json.MarshalIndent(bodyParsed, "  ", "  ")
			fmt.Printf("  Body:\n  %s\n", string(prettyBody))
		} else {
			fmt.Printf("  Body: %s\n", d.Body)
		}
	}
}

